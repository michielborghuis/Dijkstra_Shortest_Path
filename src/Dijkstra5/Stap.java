package Dijkstra5;

public class Stap {
    private String name;
    private Class<?> cls;
    private int distance;
    private int price;
    private int minutes;

    public Stap(String name, Class<?> cls, int distance, int price, int minutes){
        this.name = name;
        this.cls = cls;
        this.distance = distance;
        this.price = price;
        this.minutes = minutes;
    }


}
